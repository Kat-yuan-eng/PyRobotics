// === Phase 3: Sin/Cos Lookup ===

fp_t lut_sin(int16_t deg) {
    int16_t d = deg % 360;
    if (d < 0) d += 360;
    return sin_table[d];
}

fp_t lut_cos(int16_t deg) {
    int16_t d = 90 - deg;
    d = d % 360;
    if (d < 0) d += 360;
    return sin_table[d];
}

// === Phase 4: Atan2 Lookup ===

fp_t lut_atan2(fp_t y, fp_t x) {
    fp_t abs_y = FP_ABS(y);
    fp_t abs_x = FP_ABS(x);
    if (abs_x < FP_EPSILON && abs_y < FP_EPSILON) return 0;
    fp_t angle;
    if (abs_x >= abs_y) {
        fp_t ratio = FP_DIV(abs_y, FP_MAX(abs_x, FP_EPSILON));
        int16_t idx = (int16_t)FP_TO_INT(FP_MUL(ratio, FP_FROM_INT(90)));
        if (idx > 90) idx = 90;
        angle = atan_table[idx];
    } else {
        fp_t ratio = FP_DIV(abs_x, FP_MAX(abs_y, FP_EPSILON));
        int16_t idx = (int16_t)FP_TO_INT(FP_MUL(ratio, FP_FROM_INT(90)));
        if (idx > 90) idx = 90;
        angle = FP_HALF_PI - atan_table[idx];
    }
    if (x < 0) angle = FP_PI - angle;
    if (y < 0) angle = -angle;
    if (angle > FP_PI) angle -= FP_TWO_PI;
    if (angle <= -FP_PI) angle += FP_TWO_PI;
    return angle;
}
